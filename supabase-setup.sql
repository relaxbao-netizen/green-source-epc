-- 簽核人員 / 角色（每人一組 PIN，作為線上簽核驗證）
create table if not exists public.approvers (
  id         bigint generated by default as identity primary key,
  name       text not null,
  role       text not null,   -- 申請人主管/採購部主管/工程設計部主管/專案管理部主管/業務/協理/總經理/董事長
  dept       text,
  email      text default '',
  pin        text,            -- 簽核 PIN
  active     boolean default true,
  created_at timestamptz default now()
);
-- 既有資料庫升級
alter table public.approvers add column if not exists email text default '';

-- 申請單（含追加明細與簽核流程）
create table if not exists public.app_request (
  id             bigint generated by default as identity primary key,
  serial_no      text,                       -- 流水號
  apply_dept     text,                       -- 申請單位
  apply_date     date,
  site_name      text,                       -- 工程名稱
  applicant      text,
  reason_category text,                      -- 申請原因：客戶追加／變更…
  responsibility text,                       -- 責任歸屬：業主變更…
  cost_owner     text,                       -- 費用歸屬：可向客戶請款 / 公司吸收
  total_amount   numeric default 0,          -- 追加款總額
  amount_tier    text,                       -- 10萬以下 / 10萬~100萬 / 100萬以上
  items          jsonb default '[]'::jsonb,  -- 追加明細
  reason_text    text,                       -- 原因說明
  sign_chain     jsonb default '[]'::jsonb,  -- 簽核關卡 [{role,approver,action,comment,signed_at}]
  cur_step       int default 0,              -- 目前待簽關卡索引
  status         text default 'pending',     -- pending/approved/rejected/archived
  client_amount  numeric default 0,          -- 對客戶追加金額（業務可向客戶請款金額）
  invoice_date   text,                       -- 開票時間
  payment_date   text,                       -- 收款時間
  created_at     timestamptz default now()
);
-- 既有資料庫升級
alter table public.app_request add column if not exists client_amount numeric default 0;
alter table public.app_request add column if not exists invoice_date text;
alter table public.app_request add column if not exists payment_date text;
alter table public.app_request add column if not exists client_bills jsonb default '[]'::jsonb;

alter table public.approvers   enable row level security;
alter table public.app_request enable row level security;

create policy "public read approvers"     on public.approvers   for select using (true);
create policy "public insert approvers"   on public.approvers   for insert with check (true);
create policy "public update approvers"   on public.approvers   for update using (true);
create policy "public delete approvers"   on public.approvers   for delete using (true);
create policy "public read app_request"   on public.app_request for select using (true);
create policy "public insert app_request" on public.app_request for insert with check (true);
create policy "public update app_request" on public.app_request for update using (true);
create policy "public delete app_request" on public.app_request for delete using (true);

