create policy "public update sites"       on public.sites       for update using (true);
create policy "public delete sites"       on public.sites       for delete using (true);
create policy "public read supervisors"   on public.supervisors for select using (true);
create policy "public insert supervisors" on public.supervisors for insert with check (true);
create policy "public update supervisors" on public.supervisors for update using (true);
create policy "public delete supervisors" on public.supervisors for delete using (true);
create policy "public read weather_log"    on public.weather_log  for select using (true);
create policy "public insert weather_log"  on public.weather_log  for insert with check (true);
create policy "public update weather_log"  on public.weather_log  for update using (true);
create policy "public read supervisor_log"   on public.supervisor_log for select using (true);
create policy "public insert supervisor_log" on public.supervisor_log for insert with check (true);
create policy "public delete supervisor_log" on public.supervisor_log for delete using (true);

