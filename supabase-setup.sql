-- 案場資料表
create table if not exists public.sites (
  id          bigint generated by default as identity primary key,
  name        text not null,
  label       text default '',          -- 地圖短標籤
  region      text default 'north',     -- north / center / south / east
  city        text default '',
  cap         text default '待確認',
  owner       text default '',
  pm          text default '',          -- 工地主任
  sales       text default '',          -- 案場業務
  type        text default '太陽能',    -- 太陽能 / 儲能 / 太陽能+儲能 / 儀電配管
  addr        text default '',
  mw          numeric default 0,        -- 光電容量 kW
  skw         numeric default 0,        -- 儲能功率 kW
  mwh         numeric default 0,        -- 儲能容量 kWh
  start_date  text default '',          -- YYYY/MM/DD
  end_date    text default '',
  pct         int default 0,
  status      text default 'normal',    -- normal / warn / danger / done
  lng         numeric,                  -- 地圖經度
  lat         numeric,                  -- 地圖緯度
  wbs         jsonb,                    -- WBS 各項完成率 {"整地工程":100, ...}
  created_at  timestamptz default now()
);

-- 工地主任資料表
create table if not exists public.supervisors (
  id     bigint generated by default as identity primary key,
  name   text not null unique,
  color  text default '#3b82f6',
  phone  text default '',
  sites  bigint[] default '{}',         -- 負責的案場 id 清單
  avatar text default ''                -- 大頭貼（壓縮後的 base64 data URL）
);

-- 既有資料庫升級：補上 avatar 欄位（已存在則略過）
alter table public.supervisors add column if not exists avatar text default '';

-- 既有資料庫升級：案場補上 sales（案場業務）欄位
alter table public.sites add column if not exists sales text default '';

-- 施工晴雨表：每日各縣市上午/下午天氣記錄（自開始使用日起累積）
create table if not exists public.weather_log (
  id         bigint generated by default as identity primary key,
  log_date   date not null,
  county     text not null,
  am_weather text,
  am_pop     int,
  pm_weather text,
  pm_pop     int,
  created_at timestamptz default now(),
  unique(log_date, county)
);

-- 工地主任每日派工／支援記錄（一天可多案場；place 存非案場項目如公司/週休/颱風假…）
create table if not exists public.supervisor_log (
  id         bigint generated by default as identity primary key,
  log_date   date not null,
  supervisor text not null,
  site_id    bigint,                  -- 案場 id；特殊項目時為 null
  place      text,                    -- 非案場項目：公司/南部辦公室/週休/休假/國定假日/颱風假
  created_at timestamptz default now()
);
-- 既有資料庫升級
alter table public.supervisor_log alter column site_id drop not null;
alter table public.supervisor_log add column if not exists place text;
-- 同日同主任同案場或同項目不重複
create unique index if not exists supervisor_log_uniq
  on public.supervisor_log (log_date, supervisor, coalesce(site_id, -1), coalesce(place, ''));

-- 啟用 Row Level Security
alter table public.sites enable row level security;
alter table public.supervisors enable row level security;
alter table public.weather_log enable row level security;
alter table public.supervisor_log enable row level security;

